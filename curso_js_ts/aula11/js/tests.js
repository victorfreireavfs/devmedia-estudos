let num1 = prompt('digite o primeiro numero')
let num2 = prompt('digite o segundo numero')

num1 = Number(num1)
num2 = Number(num2)

let soma = num1 + num2
let mult = num1 * num2
let div = num1 / num2

alert (`a soma dos dois numeros digitados é: ${soma}`)
alert (`a multiplicação dos dois numeros digitados é: ${mult}`)
alert (`a divisão dos dois numeros digitados é: ${div}`)